    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String item = (String) jComboBox1.getSelectedItem();
        int valor = (int) jSpinner1.getValue();
        float kcalorias = 0;
        
        switch (item){
            case "Manzana":
                kcalorias = 52 * valor;
                salud.agregarCalorias(kcalorias);
                break;
            case "Pan":
                kcalorias = 175 * valor;
                salud.agregarCalorias(kcalorias);
                break;
            case "Bife":
                kcalorias = 121 * valor;
                salud.agregarCalorias(kcalorias);
                break;
            case "Pollo":
                kcalorias = 150 * valor;
                salud.agregarCalorias(kcalorias);
                break;
            case "Chocolate":
                kcalorias = 556 * valor;
                salud.agregarCalorias(kcalorias);
                break;
        }
        
        calcon.setText(Float.toString(kcalorias) + "cal");
        calto.setText(Float.toString(salud.getCaloriasconsumidas()));
    }//GEN-LAST:event_jButton1ActionPerformed

