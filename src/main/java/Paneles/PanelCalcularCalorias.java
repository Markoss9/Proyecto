    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        float calQuemadas = Float.parseFloat(calque.getText());
        salud.setCaloriasquemadas(calQuemadas);
        float balance = salud.getCaloriasconsumidas() - salud.getCaloriasquemadas();
        balcalo.setText(Float.toString(balance));
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try{
            if (conexion == null || conexion.isClosed()){
                JOptionPane.showMessageDialog(this, "Error: No hay conexión a la base de datos.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }else {
                float calCon = salud.getCaloriasconsumidas();
                float calQue = Float.parseFloat(calque.getText());
                System.out.println(calCon);
                System.out.println(calQue);
                salud.guardarCalorias(calCon, calQue);
                JOptionPane.showMessageDialog(this, "Datos guardados exitosamente");
            }
        }catch(SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al guardar en la base de datos: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

