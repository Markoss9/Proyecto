    private void imcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_imcActionPerformed
        float peso = Float.parseFloat(campopeso.getText());
        salud.setPeso(peso);
        float altura = Float.parseFloat(campoaltura.getText());
        salud.setAltura(altura);
        
        float imc = salud.calcularIMC();
        imclabel.setText(String.format("%.2f", imc));
        
        String resumen = salud.evaluarIMC();
        resumenlabel.setText(resumen);
    }//GEN-LAST:event_imcActionPerformed

    private void GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarActionPerformed
        try{
            if (conexion == null || conexion.isClosed()){
                JOptionPane.showMessageDialog(this, "Error: No hay conexi√≥n a la base de datos.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }else{
                float Peso = Float.parseFloat(campopeso.getText());
                float Altura = Float.parseFloat(campoaltura.getText());
                salud.guardarDatosPesoyAltura();
                JOptionPane.showMessageDialog(this, "Datos guardados exitosamente");
            }
            
        }catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al guardar en la base de datos: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_GuardarActionPerformed

