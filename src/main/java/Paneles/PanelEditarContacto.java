    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        int id = Integer.parseInt(idEdit.getText());
        String nombre = nomEdit.getText();
        String apellido = apeEdit.getText();
        String telefono = telEdit.getText();
        String correo = corEdit.getText();
        
        boolean result = Contactos.actualizarContacto(id, nombre, apellido, telefono, correo);
        
        if (result){
            System.out.println("Contacto con Id: "+id+" editado correctamente");
            JOptionPane.showMessageDialog(this, "Contacto editado correctamente");
        }else{
            System.out.println("No se encontro el contacto con Id: "+id);
            JOptionPane.showMessageDialog(this, "No se encontro el contacto");
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

