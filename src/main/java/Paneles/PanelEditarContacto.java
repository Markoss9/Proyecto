    public void cargarDatosTabla() throws SQLException{
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        
        ArrayList<Contactos> lista = Contactos.listarContactos(this.dniUsuario);
        for (Contactos contacto : lista){
            model.addRow(new Object[]{
                contacto.getId(),
                contacto.getNombre(), contacto.getApellido(),
                contacto.getEmail(), contacto.getTelefono()
            }
            );
        }  
    }
    
