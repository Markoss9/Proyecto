    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        
        try{
            if (conexion == null || conexion.isClosed()){
                JOptionPane.showMessageDialog(this, "Error: No hay conexi√≥n a la base de datos.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }else{
                
                LocalDate fechaActual = LocalDate.now();
                DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");
                String fechaFormateada = fechaActual.format(formato);
                metas.setFechaInicio(fechaFormateada);
                String descripcion = jTextArea1.getText();
                String fechaLimite = jTextField1.getText();
                metas.guardarMeta(this.dniUsuario,descripcion, fechaLimite);
                JOptionPane.showMessageDialog(this, "Datos guardados exitosamente");
                
            }
        } catch (SQLException e) {
            Logger.getLogger(PanelAgregarMetas.class.getName()).log(Level.SEVERE, null, e);
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

