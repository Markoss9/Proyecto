    public void cargarDatosTabla() throws SQLException{
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        
        ArrayList<Metas> lista = Metas.listarMetas(this.dniUsuario);
        for (Metas metas : lista){
            model.addRow(new Object[]{
                metas.getId(),
                metas.getDescripcion(), metas.getFechaInicio(),
                metas.getFechaLimite(), metas.getProgreso()
            }
            );
        }  
    }
